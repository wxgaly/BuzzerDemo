/*
**********************************************************************
*                        函数主程序
**********************************************************************
*/
void main(void)
{
	uchar i, j;
	EA = 1;
	ET0 = 1;
	TMOD = 0x00;
	
	while(1)
  {  
		i = 0;
		while(sound_birthday[i] != 0xff)
		{
			C = 460830 / sound_birthday[i];
			TH0 = (8192 - C) / 32;
			TL0 = (8192 - C) % 32;
			TR0 = 1;
			
			for(j = 0; j < JP_birthday[i]; j++)
			{
				delay200ms();
			}
			
			TR0 = 0;
			i++;
		}      
  }
}

